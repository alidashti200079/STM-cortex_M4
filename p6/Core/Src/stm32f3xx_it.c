int move = 0;

extern RTC_HandleTypeDef hrtc;

extern RTC_DateTypeDef mydate;
extern RTC_TimeTypeDef mytime;

extern uint8_t Time[5];
extern uint8_t Date[9];

int firstSec = 0;
int Sec = 0;
int led = 0;

void motionDetection(int move) {
	if ( move > 0 ) {
		HAL_RTC_GetTime(&hrtc, &mytime, RTC_FORMAT_BIN);
	  	HAL_RTC_GetDate(&hrtc, &mydate, RTC_FORMAT_BIN);

	  	setCursor(20,1);
		sprintf(Time, "%2d:%2d:%2d ", mytime.Hours, mytime.Minutes, mytime.Seconds);
		print(Time);

		sprintf(Date, "20%d/%d/%d", mydate.Year, mydate.Month, mydate.Date);
		print(Date);

		warning();

		move = 0;
		led = 1;
	}
}

void changeLED() {

	if( led == 1 ) {
		HAL_GPIO_WritePin(GPIOC, GPIO_PIN_6, 0);
		led = 0;
	} else if (led == 0 ){
		HAL_GPIO_WritePin(GPIOC, GPIO_PIN_6, 1);
	}

}


